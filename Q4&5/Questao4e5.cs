

Int32 numero;
numero = Convert.ToInt16(!(21 > 21));
Console.WriteLine(numero);

//A saída do código é 0.

// A variável numero é declarada como um inteiro de 32 bits.
// A expressão !(21 > 21) é avaliada como false, pois 21 não é maior que 21, mas como esse boolean está sendo negado pelo " ! ".
// Então o resultado impresso no terminal será 1.
// O operador Convert.ToInt16() converte um valor para um inteiro de 16 bits;

